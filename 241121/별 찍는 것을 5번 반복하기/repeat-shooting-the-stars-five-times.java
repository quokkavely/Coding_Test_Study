public class Main {
    public static void main(String[] args) {
        // 여기에 코드를 작성해주세요.
        printTenStars();
        printTenStars();
        printTenStars();
        printTenStars();
        printTenStars();
    }

    public static void printTenStars() {
       for(int i = 0; i < 10; i++) {
            System.out.print("*");
       }

       System.out.println();
    }
}